#!/usr/bin/env node

/**
 * Scans public/images/partners/vacation-rental-hero/ and generates
 * src/generated/vacation-rental-hero-media.ts with the file list.
 *
 * Drop images (.webp, .jpg, .jpeg, .png, .avif) into the folder, then
 * push or run `npm run hero:refresh` (the build runs this automatically).
 *
 * Filenames become alt text — name files descriptively, e.g.
 *   "lake-travis-pool-deck.webp" → alt="Lake travis pool deck"
 */

const fs = require('fs');
const path = require('path');

const SRC_DIR = path.join(__dirname, '..', 'public', 'images', 'partners', 'vacation-rental-hero');
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'generated', 'vacation-rental-hero-media.ts');

const IMAGE_EXTS = new Set(['.webp', '.jpg', '.jpeg', '.png', '.avif']);

function filenameToAlt(filename) {
  const name = path.parse(filename).name;
  const words = name.replace(/[-_]/g, ' ').replace(/\s+/g, ' ').trim();
  return words.charAt(0).toUpperCase() + words.slice(1);
}

if (!fs.existsSync(SRC_DIR)) {
  fs.mkdirSync(SRC_DIR, { recursive: true });
}

const files = fs.readdirSync(SRC_DIR)
  .filter(f => IMAGE_EXTS.has(path.extname(f).toLowerCase()) && !f.startsWith('.'))
  .sort();

const items = files.map(f => ({
  src: `/images/partners/vacation-rental-hero/${f}`,
  alt: filenameToAlt(f),
}));

const outputDir = path.dirname(OUTPUT_FILE);
if (!fs.existsSync(outputDir)) fs.mkdirSync(outputDir, { recursive: true });

const tsContent = `// Auto-generated by scripts/scan-vacation-rental-hero.js — do not edit manually.
// Last scanned: ${new Date().toISOString()}
//
// To change images: drop/delete files in public/images/partners/vacation-rental-hero/
// then run \`npm run hero:refresh\` (the production build runs this automatically).

export interface VacationRentalHeroMedia {
  src: string;
  alt: string;
}

export const vacationRentalHeroMedia: VacationRentalHeroMedia[] = ${JSON.stringify(items, null, 2)};
`;

fs.writeFileSync(OUTPUT_FILE, tsContent);
console.log(`✓ Generated ${path.relative(process.cwd(), OUTPUT_FILE)} (${items.length} image${items.length === 1 ? '' : 's'})`);
if (items.length === 0) {
  console.log(`  Drop images into: public/images/partners/vacation-rental-hero/`);
}
